#include <stdio.h>
#include <math.h> // ใช้สำหรับฟังก์ชัน fmod()

int main() {
    // ประกาศตัวแปรชนิด double สำหรับเก็บค่าทศนิยม
    double x = 10, y = 3, z = 2, a = 4, rate = 5;
    double total = 100, price = 20, quantity = 3, discount = 10;
    double score = 80, penalty = 2;
    int mistake = 1;

   
    // 1. ลบค่าออกจากตัวแปร
    // แบบเต็ม
    x = x - 4.0;  // ลบ 4.0 จากค่า x เดิม
    // แบบย่อ
    x -= 4.0;     // ใช้ -= แทนการเขียนแบบเต็ม

    
    // 2. คูณค่าด้วยตัวเลข
    // แบบเต็ม
    x = 6.5 * x;  // คูณค่า x ด้วย 6.5
    // แบบย่อ
    x *= 6.5;     // ใช้ *= เพื่อคูณและกำหนดค่าให้ x

   
    // 3. หารเอาเศษ (แบบทศนิยม)
    // แบบเต็ม
    x = fmod(x, (y + z * a));  // ใช้ fmod เพื่อหารเอาเศษของตัวเลขทศนิยม
    // แบบย่อ
    x = fmod(x, (y + z * a));  // fmod() ไม่มี shorthand operator จึงต้องใช้แบบนี้เหมือนเดิม

    
    // 4. หารค่าด้วยตัวเลข
    // แบบเต็ม
    x = x / (2.0 * x);    // หารค่า x ด้วย (2.0 * x)
    // แบบย่อ
    x /= (2.0 * x);       // ใช้ /= เพื่อหารและกำหนดค่าให้ x

    
    // 5. บวกค่าผลคูณและลบส่วนลด
    // แบบเต็ม
    total = total + (price * quantity - discount); // เพิ่มค่า total ตามสูตร
    // แบบย่อ
    total += (price * quantity - discount);        // ใช้ += เพื่อบวกและกำหนดค่า

    
    // 6. คูณเพิ่มตามเปอร์เซ็นต์
    // แบบเต็ม
    x = x * (1 + rate / 100);  // คูณ x ด้วย (1 + อัตราร้อยละ)
    // แบบย่อ
    x *= (1 + rate / 100);     // ใช้ *= เพื่อคูณและกำหนดค่า

   
    // 7. ลบคะแนนตาม mistake
    // แบบเต็ม
    score = score - (penalty * (mistake + 1));  // ลบค่าปรับออกจาก score
    // แบบย่อ
    score -= (penalty * (mistake + 1));         // ใช้ -= เพื่อหักคะแนน

   
    // แสดงผลลัพธ์สุดท้าย
    printf("x = %.2f\n", x);
    printf("total = %.2f\n", total);
    printf("score = %.2f\n", score);

    return 0;
}
