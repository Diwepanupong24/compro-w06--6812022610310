#include <stdio.h>   // สำหรับ printf
#include <math.h>    // สำหรับ fmod()

int main() {
    // กำหนดค่าตัวแปร
    int a = 5, b = 17; // a และ b เป็นจำนวนเต็ม
    float c = 8.5, d = 4.0; // c และ d เป็นจำนวนจริง (float)

    // บรรทัดที่ 1: แสดงค่าเริ่มต้นของตัวแปรทั้งหมด
    printf("a = %d, b = %d, c = %.2f, d = %.2f\n", a, b, c, d);

    // บรรทัดที่ 2: ผลการคำนวณของ d + a
    printf("d + a = %.2f\n", d + a); // d เป็น float, a เป็น int ผลรวมได้ค่าออกมาเป็น float

    // บรรทัดที่ 3: ผลการคำนวณของ a - b
    printf("a - b = %d\n", a - b); // ลบค่าจำนวนเต็ม

    // บรรทัดที่ 4: ผลการคำนวณของ c * d
    printf("c * d = %.2f\n", c * d); // คูณค่าจำนวนจริง ทศนิยม 2 ตำแหน่ง

    // บรรทัดที่ 5: ผลการคำนวณของ a * c
    printf("a * c = %.2f\n", a * c); // int * float ผลลัพธ์เป็น float ทศนิยม 2 ตำแหน่ง

    // บรรทัดที่ 6: ผลการคำนวณของ c / d
    printf("c / d = %.2f\n", c / d); // หารค่าจำนวนจริง ทศนิยม 2 ตำแหน่ง

    // บรรทัดที่ 7: ผลการคำนวณของ b / c
    printf("b / c = %.2f\n", b / c); // int / float ผลลัพธ์เป็น float ทศนิยม 2 ตำแหน่ง

    // บรรทัดที่ 8: ผลการคำนวณของ a % b
    printf("a %% b = %d\n", a % b);  // หาเศษของ a หาร b

    // บรรทัดที่ 9: ใช้ fmod() แทน c % a
    printf("fmod(c, a) = %.2f\n", fmod(c, a)); //แก้ไขแล้วใช้ฟังก์ชัน fmod() จาก math.h

    // บรรทัดที่ 10: ใช้ fmod() แทน c % d
    printf("fmod(c, d) = %.2f\n", fmod(c, d)); //แก้ไขแล้วใช้ฟังก์ชัน fmod() จาก math.h

    return 0;
}
