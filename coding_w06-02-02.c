#include <stdio.h>

int main() {
    // บรรทัดที่ 1: กำหนดค่าเริ่มต้น x = 1.0, y = 2.0
    double x = 1.0, y = 2.0, z;

    // บรรทัดที่ 2: x = y + 5.0 → x = 2.0 + 5.0 = 7.0
    x = y + 5.0;
    printf("หลังบรรทัดที่ 2: x=%.2f, y=%.2f, z=ไม่ได้กำหนด\n", x, y);

    // บรรทัดที่ 3: y = x / 2.0 → y = 7.0 / 2.0 = 3.5
    y = x / 2.0;
    printf("หลังบรรทัดที่ 3: x=%.2f, y=%.2f, z=ไม่ได้กำหนด\n", x, y);

    // บรรทัดที่ 4: y = (x * 3.0) + 4.0 → y = (7.0 * 3.0) + 4.0 = 25.0
    y = (x * 3.0) + 4.0;
    printf("หลังบรรทัดที่ 4: x=%.2f, y=%.2f, z=ไม่ได้กำหนด\n", x, y);

    // บรรทัดที่ 5: x = -0.5 - y → x = -0.5 - 25.0 = -25.5
    x = -0.5 - y;
    printf("หลังบรรทัดที่ 5: x=%.2f, y=%.2f, z=ไม่ได้กำหนด\n", x, y);

    // บรรทัดที่ 6: z = x + y → z = -25.5 + 25.0 = -0.5
    z = x + y;
    printf("หลังบรรทัดที่ 6: x=%.2f, y=%.2f, z=%.2f\n", x, y, z);

    return 0;
}
