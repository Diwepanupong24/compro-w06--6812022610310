#include <stdio.h>

int main() {
    int a = 5, b = 2;
    float x = 3.0, y = 4.5;

    int r1, r2;
    float r3, r4;

    //   r1 = a++ + b + (int)y % 3;
    //   ขั้นตอน:
    //   เริ่มจาก a++ ก่อน อันนี้หมายความว่าใช้ค่า a เดิมก่อน (คือ 5)
    //   พอใช้เสร็จก็ค่อยเอา a ไปบวกเพิ่มอีก 1 กลายเป็น 6
    //   จากนั้น (int)y ก็คือเอาค่า y มาแปลงเป็นจำนวนเต็ม ได้ค่า 4
    //   แล้วเอา 4 ไปหารเอาเศษด้วย 3 ได้เศษเป็น 1
    //   สุดท้ายเอามาบวกกันทั้งหมด: 5 + 2 + 1 = 8
    r1 = a++ + b + (int)y % 3; 
    printf("r1 = %d\n", r1);

    //   r2 = (a > b) && ((int)x / b < 2);
    //   ตอนนี้ a มีค่า 6
    //   เช็ก (a > b) ก็คือดูว่า 6 มากกว่า 2 ไหม → คำตอบคือจริง (ได้ 1)
    //   จากนั้นเอา x มาแปลงเป็นจำนวนเต็ม (int)x = 3
    //   นำ 3 มาหารกับ b (ซึ่ง b = 2) → 3 / 2 = 1 (เพราะหารจำนวนเต็ม)
    //   เช็กว่า 1 น้อยกว่า 2 ไหม → จริง (ได้ 1)
    //   สุดท้ายเอาผลทั้งสองมาทำ && (และเงื่อนไข)
    //   1 && 1 → ได้ 1 (หมายถึงเงื่อนไขทั้งคู่เป็นจริง)
    r2 = (a > b) && ((int)x / b < 2);
    printf("r2 = %d\n", r2);

    //   r3 = ++x * y - a / 2;
    //   เริ่มจาก ++x หมายถึงเพิ่มค่า x ขึ้น 1 ก่อนใช้
    //   เดิม x = 3.0 → พอ ++x แล้วกลายเป็น 4.0
    //   เอา 4.0 มาคูณกับ y (ซึ่ง y = 4.5) → ได้ 18.00
    //   จากนั้นคำนวณ a / 2 → a = 6, ดังนั้น 6 / 2 = 3
    //   สุดท้ายเอา 18.00 ลบด้วย 3 → ได้ผลลัพธ์ 15.00
    r3 = ++x * y - a / 2;
    printf("r3 = %.2f\n", r3);

    //   r4 = ((x += 1.5) > y) || (b-- > 0);
    //   เริ่มจาก x += 1.5 หมายถึงเอา x บวกเพิ่ม 1.5
    //   เดิม x = 4.0 → บวก 1.5 ได้ x = 5.5
    //   เช็กว่า 5.5 มากกว่า y (ซึ่ง y = 4.5) ไหม → คำตอบคือจริง (ได้ 1)
    //   เนื่องจากเป็น OR (||) ถ้าด้านหน้าเป็นจริงแล้ว ก็จะไม่ไปประเมินด้านหลัง (b-- > 0)
    //   ดังนั้น b จะไม่ถูกลดค่า และผลสุดท้าย r4 = 1.00
    r4 = ((x += 1.5) > y) || (b-- > 0); 
    printf("r4 = %.2f\n", r4);

    return 0;
}
