#include <stdio.h>

int main() {
    // บรรทัดที่ 1: กำหนดค่าเริ่มต้นให้ i และ j, ส่วน k ยังไม่กำหนด
    int i = 1, j = 2, k;

    // บรรทัดที่ 2: บวก i และ j แล้วเก็บลงใน k โดย k = 1 + 2 = 3
    k = i + j;
    printf("หลังบรรทัดที่ 2: i=%d, j=%d, k=%d\n", i, j, k);

    // บรรทัดที่ 3: คำนวณ k * j ก่อน → 3 * 2 = 6 แล้วบวกกับ i → i = 1 + 6 = 7
    i = i + (k * j);
    printf("หลังบรรทัดที่ 3: i=%d, j=%d, k=%d\n", i, j, k);

    // บรรทัดที่ 4: j = i / 2 → 7 / 2 = 3.5 แต่ชนิดข้อมูลเป็น int เลยได้ j = 3 
    j = i / 2;
    printf("หลังบรรทัดที่ 4: i=%d, j=%d, k=%d\n", i, j, k);

    // บรรทัดที่ 5: k = i % 2 → 7 % 2 = 1 (เศษจากการหาร)
    k = i % 2;
    printf("หลังบรรทัดที่ 5: i=%d, j=%d, k=%d\n", i, j, k);

    // บรรทัดที่ 6: (j + k) * 3 → (3 + 1) * 3 = 12 → i = 12
    i = (j + k) * 3;
    printf("หลังบรรทัดที่ 6: i=%d, j=%d, k=%d\n", i, j, k);

    return 0;
}
