#include <stdio.h>

int main() {
    int x = 12, y = 7, z = 12;

    // เงื่อนไขที่ 1: x > y → 12 > 7 → จริง
    printf("1. x > y = %d\n", x > y);

    // เงื่อนไขที่ 2: x < z → 12 < 12 → เท็จ
    printf("2. x < z = %d\n", x < z);

    // เงื่อนไขที่ 3: x == z → 12 == 12 → จริง
    printf("3. x == z = %d\n", x == z);

    // เงื่อนไขที่ 4: x != y → 12 != 7 → จริง
    printf("4. x != y = %d\n", x != y);

    // เงื่อนไขที่ 5: !(2*5 >= y) || (5 != (5/3))
    //  → !(10 >= 7) || (5 != 1) → !1 || 1 → 0 || 1 → 1
    printf("5. !(2*5 >= y) || (5 != (5/3)) = %d\n", !(2*5 >= y) || (5 != (5/3)));

    // เงื่อนไขที่ 6: !(x < y) → !(12 < 7) → !0 → 1
    printf("6. !(x < y) = %d\n", !(x < y));

    // เงื่อนไขที่ 7: ((x + y) > (z * 2)) → (19 > 24) → 0
    printf("7. (x + y) > (z * 2) = %d\n", (x + y) > (z * 2));

    // เงื่อนไขที่ 8: (x % 2 == 0) || (y % 2 == 1)
    // → (12 % 2 == 0) || (7 % 2 == 1) → 1 || 1 → 1
    printf("8. (x %% 2 == 0) || (y %% 2 == 1) = %d\n", (x % 2 == 0) || (y % 2 == 1));

    // เงื่อนไขที่ 9: (x > y) && (z < y)
    // → (12 > 7) && (12 < 7) → 1 && 0 → 0
    printf("9. (x > y) && (z < y) = %d\n", (x > y) && (z < y));

    return 0;
}
